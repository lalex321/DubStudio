"""Generate docs/DubStudio_Guide.docx — краткое описание + руководство
пользователя.

Запуск:
    python scripts/build_guide.py

Требует python-docx (`pip install python-docx`). Документ — статичный
артефакт; при изменениях UI правь содержимое тут и перегенерируй.
"""
from __future__ import annotations

from pathlib import Path

from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH


OUTPUT = Path(__file__).resolve().parent.parent / "docs" / "DubStudio_Guide.docx"


def _set_default_font(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(11)


def _h1(doc: Document, text: str) -> None:
    p = doc.add_heading(text, level=1)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT


def _h2(doc: Document, text: str) -> None:
    doc.add_heading(text, level=2)


def _h3(doc: Document, text: str) -> None:
    doc.add_heading(text, level=3)


def _p(doc: Document, text: str) -> None:
    doc.add_paragraph(text)


def _bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style="List Bullet")


def _numbered(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style="List Number")


def build() -> Path:
    doc = Document()
    _set_default_font(doc)

    # ---- Title ---------------------------------------------------------
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.add_run("DubStudio")
    run.bold = True
    run.font.size = Pt(28)

    sub = doc.add_paragraph()
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = sub.add_run("Краткое описание и руководство пользователя")
    sub_run.italic = True
    sub_run.font.size = Pt(14)

    doc.add_paragraph()  # spacer

    # ---- I. Краткое описание ------------------------------------------
    _h1(doc, "I. Краткое описание")

    _p(
        doc,
        "DubStudio — веб-приложение для помощников режиссёра дубляжа. "
        "Автоматизирует учёт количества слов в эпизодах сериалов и "
        "фильмов, назначение актёров на роли и формирование отчётов "
        "для бухгалтерии."
    )

    _h3(doc, "Для кого")
    _p(
        doc,
        "Помрежи студии дубляжа, работающие с проектами Netflix и "
        "других студий. Часто — несколько сериалов одновременно, "
        "еженедельные новые версии xlsx-файлов с word count."
    )

    _h3(doc, "Что делает")
    _numbered(
        doc,
        [
            "Импорт xlsx-файлов word count по сериям. Файлы парсятся "
            "прямо в браузере — на сервер уходит только агрегированный "
            "JSON, исходный xlsx Netflix наружу не отправляется.",
            "Назначение актёров на персонажей и инлайн-редактирование "
            "значений word count.",
            "Отслеживание новых персонажей при повторном импорте — "
            "помреж видит, что добавилось, и решает: оставить, "
            "объединить с существующим или удалить.",
            "Скачивание сводного xlsx и отчёта для бухгалтерии.",
            "Хранение списка актёров с Telegram handles для будущей "
            "рассылки уведомлений.",
        ],
    )

    _h3(doc, "Workflow одной фразой")
    _p(
        doc,
        "Получили xlsx → импортировали в проект → назначили актёров → "
        "выгрузили отчёт."
    )

    _h3(doc, "Технические основы")
    _bullets(
        doc,
        [
            "FastAPI + SQLModel поверх SQLite (на проде Render — "
            "постоянный диск).",
            "Jinja2 + vanilla JS, без React.",
            "xlsx-парсер SheetJS работает в браузере. Сервер видит "
            "только агрегированные числа, не сами файлы.",
            "Авторизация — сессионные cookie.",
        ],
    )

    doc.add_page_break()

    # ---- II. Руководство пользователя ---------------------------------
    _h1(doc, "II. Руководство пользователя")

    # 1. Вход
    _h2(doc, "1. Вход в систему")
    _p(
        doc,
        "Открой URL приложения. На странице Sign in введи Username "
        "(обычно email) и Password, нажми Sign in."
    )
    _p(
        doc,
        "В правом верхнем углу — кнопка переключения темы (☾ / ☀). "
        "Тёмная тема включается одним кликом, выбор сохраняется в "
        "браузере."
    )
    _p(doc, "Кнопка Sign out — выход из сессии.")

    # 2. Создание проекта
    _h2(doc, "2. Создание проекта")
    _p(doc, "Меню → Projects → + New project.")
    _p(
        doc,
        "Откроется страница с предложением выбрать xlsx-файлы. Выдели "
        "все файлы серий проекта одним выделением. Каждый файл — одна "
        "серия. Имя файла должно содержать номер эпизода в одном из "
        "распознаваемых форматов (Netflix DUB_SCRIPT, S01E05, Episode 5, "
        "«5 серия» и т.п.)."
    )
    _p(doc, "После выбора:")
    _numbered(
        doc,
        [
            "Файлы парсятся в браузере (виден прогресс).",
            "Появляется поле «Detected N episodes. Confirm the project "
            "title» с предложенным названием.",
            "Поправь название если нужно и нажми Create project.",
        ],
    )

    # 3. Страница проекта
    _h2(doc, "3. Страница проекта")
    _p(
        doc,
        "Заголовок: название проекта, номер, дата старта, количество "
        "эпизодов."
    )
    _p(doc, "Кнопки справа:")
    _bullets(
        doc,
        [
            "↓ Report — скачать xlsx-отчёт для бухгалтерии (Actor → Words).",
            "↓ Export Excel — полный xlsx со сводной таблицей.",
            "↑ Import EPs — добавить новые серии или перезаписать "
            "существующие.",
        ],
    )
    _p(doc, "Под заголовком три таба:")
    _bullets(
        doc,
        [
            "Transcription WC — основная метрика (слова в переписи). "
            "Бухгалтерия обычно использует именно её.",
            "Dialog WC — слова в диалогах.",
            "Report — превью отчёта Actor → Words.",
        ],
    )

    # 4. Грид
    _h2(doc, "4. Работа с гридом")
    _p(doc, "Колонки: Character | Actor | E01..E0N | Total | EPs.")
    _bullets(
        doc,
        [
            "Сортировка: клик по заголовку колонки → ↑/↓.",
            "Ресайз колонки: потяни правый край заголовка. Ширина "
            "сохраняется в браузере.",
            "Редактирование актёра: клик в ячейку Actor → введи имя → "
            "blur (клик в другое место) или Enter сохраняет. Escape "
            "отменяет.",
            "Ручная правка word count: клик в ячейку с числом → введи "
            "новое → blur / Enter. Это пометит ячейку как edited — "
            "повторный импорт не перезатрёт.",
        ],
    )

    # 5. Повторный импорт
    _h2(doc, "5. Повторный импорт серий")
    _p(
        doc,
        "Кнопка ↑ Import EPs на странице проекта. Выбери xlsx-файлы — "
        "новые добавятся, существующие перезапишутся."
    )
    _p(doc, "Что важно при повторном импорте:")
    _bullets(
        doc,
        [
            "Существующие word count перезаписываются — кроме тех, "
            "что ты руками поправил (помечены edited и не трогаются).",
            "Новые персонажи появляются с красным значком ⚠ «New "
            "character after import».",
        ],
    )
    _p(doc, "Рядом с новым персонажем три кнопки:")
    _bullets(
        doc,
        [
            "✓ Keep — оставить как есть, отдельный персонаж.",
            "→ Merge — открывает поле ввода имени существующего "
            "персонажа (с автокомплитом). После Merge word count и "
            "actor-assignment объединяются.",
            "✕ Delete — удалить персонажа целиком (с подтверждением).",
        ],
    )

    # 6. Report tab
    _h2(doc, "6. Таб Report")
    _p(
        doc,
        "Превью того же, что в xlsx-отчёте: Actor + Words. Сортировка "
        "по любой колонке, по дефолту по имени актёра. Если в проекте "
        "ещё нет назначений — будет надпись «No actor assignments yet»."
    )

    # 7. Report download
    _h2(doc, "7. Скачивание отчёта (↓ Report)")
    _p(
        doc,
        "Скачивает xlsx с двумя колонками (Actor, Words), отсортированными "
        "по имени, с включённым auto-filter в шапке. Бухгалтер открывает "
        "в Excel и фильтрует/сортирует своими инструментами."
    )

    # 8. Full export
    _h2(doc, "8. Полный экспорт (↓ Export Excel)")
    _p(
        doc,
        "Скачивает развёрнутый xlsx с теми же данными, что в гриде на "
        "странице проекта: сводная по персонажам, эпизодам и актёрам. "
        "Файл содержит auto_filter и формулы SUM/COUNT — те, что Excel "
        "расставил бы вручную."
    )

    # 9. Clipboard tricks
    _h2(doc, "9. Excel-style работа с буфером обмена")

    _h3(doc, "Копировать колонку Actor")
    _p(
        doc,
        "В шапке колонки Actor — кнопка ⎘. По клику все имена актёров "
        "(в текущем порядке сортировки) кладутся в буфер обмена по "
        "одному на строку. Вставь в Excel — попадёт в столбец."
    )

    _h3(doc, "Вставить колонку из Excel в DubStudio")
    _p(
        doc,
        "Скопируй столбец имён в Excel (Cmd+C / Ctrl+C). Кликни в "
        "первую ячейку Actor, нажми Cmd+V. Появится подтверждение «Paste "
        "N actors starting from this row?» с предупреждением о новых "
        "актёрах, которые будут созданы. Подтверди — заполнится сверху "
        "вниз последовательно."
    )

    _h3(doc, "Прямоугольное выделение")
    _p(
        doc,
        "Кликни в любую ячейку грида — это anchor. Shift+click в другую "
        "— подсветится прямоугольник от anchor до неё. Cmd+C / Ctrl+C "
        "копирует выделение как TSV (табы между колонками, переносы "
        "между строками). Вставь в Excel или Google Sheets — встанет "
        "блоком. Escape или клик без shift сбрасывают выделение."
    )

    _p(
        doc,
        "Важно: порядок строк при copy/paste определяется текущей "
        "сортировкой грида. Если переносишь данные туда-сюда — сортируй "
        "обе таблицы одинаково (обычно по Character A→Z)."
    )

    _h3(doc, "Очистка актёров на выделении")
    _p(
        doc,
        "При активном прямоугольном выделении нажми Delete (или "
        "Backspace) — появится подтверждение «Clear actor assignment "
        "for N character(s)?». На OK все ячейки Actor в выделении "
        "очистятся (assignments снимутся). Ячейки других колонок "
        "(Character, E0N, Total, EPs) игнорируются — они не "
        "редактируются этим жестом. Если фокус сейчас в каком-то "
        "поле ввода, Delete работает как обычно (удаляет символ), "
        "не задевая выделение."
    )

    # 10. Undo / Redo
    _h2(doc, "10. Undo / Redo")
    _p(
        doc,
        "Cmd+Z (Mac) или Ctrl+Z (Windows/Linux) откатывают последнюю "
        "правку актёра. Cmd+Shift+Z / Ctrl+Shift+Z (или Ctrl+Y) — redo."
    )
    _p(doc, "Что считается одной операцией:")
    _bullets(
        doc,
        [
            "Правка одной ячейки Actor — одна операция.",
            "Вставка колонки из буфера (Cmd+V) — одна операция, "
            "независимо от количества заполненных строк. Один Cmd+Z "
            "откатит весь paste разом.",
            "Очистка диапазона через Delete — тоже одна операция, "
            "Cmd+Z вернёт всех актёров.",
        ],
    )
    _p(doc, "Ограничения:")
    _bullets(
        doc,
        [
            "Скоуп — только actor-правки. Ручная правка word count, "
            "merge / delete / acknowledge персонажей, импорт серий "
            "под undo не подпадают.",
            "Стек живёт в рамках одной открытой страницы. Перезагрузка "
            "(F5) или переход в другой проект — стек обнуляется.",
            "Когда фокус в каком-то поле ввода, Cmd+Z работает по-"
            "обычному (откатывает ввод символов в этом поле). Чтобы "
            "вызвать undo по нашему стеку — кликни мимо инпутов.",
        ],
    )

    # 11. Projects list
    _h2(doc, "11. Список проектов")
    _p(doc, "Меню → Projects. Колонки: # / Title / Episodes / Start date.")
    _bullets(
        doc,
        [
            "Сортировка по любой колонке.",
            "Клик по строке открывает проект.",
            "Только админ: галочки слева → кнопка Delete (N) наверху "
            "удаляет выбранные проекты с подтверждением.",
        ],
    )

    # 12. Admin
    _h2(doc, "12. Админка")
    _p(doc, "Меню → Admin (только для пользователя с ролью admin).")

    _h3(doc, "Вкладка Actors (по умолчанию)")
    _p(doc, "Колонки: # / Name / TLG / Assignments / [действия].")
    _bullets(
        doc,
        [
            "Добавить актёра: форма наверху, поле Name → + Add.",
            "Переименовать: клик в имя → правка → blur или Enter. "
            "Escape — отмена.",
            "Telegram handle: клик в TLG → введи username (можно с @ "
            "или без) → blur. Хранится без @.",
            "Удалить актёра: кнопка Delete справа. Если актёр назначен "
            "в проектах — все его assignments удалятся вместе с ним. "
            "Подтверждение покажет в скольких проектах.",
        ],
    )

    _h3(doc, "Вкладка Users")
    _bullets(
        doc,
        [
            "Добавить пользователя: email + password + role "
            "(assistant_director или admin) → + Add.",
            "Удалить: кнопка Delete (себя удалить нельзя).",
        ],
    )

    # 13. Tips
    _h2(doc, "13. Полезные подсказки")

    _h3(doc, "Что фильтруется при импорте")
    _p(
        doc,
        "Netflix-файлы содержат служебные строки PRINCIPAL PHOTOGRAPHY, "
        "GRAPHICS INSERTS, MAIN TITLE — они автоматически отбрасываются "
        "и не попадают в список персонажей."
    )
    _p(
        doc,
        "Если в xlsx есть строка с word count, но без имени персонажа "
        "— она проходит как (unnamed). Можешь объединить с реальным "
        "персонажем через Merge или удалить."
    )

    _h3(doc, "Что значит edited")
    _p(
        doc,
        "Ячейка word count, отредактированная вручную, помечается "
        "флагом edited и НЕ перезатирается при повторном импорте свежего "
        "xlsx. Это страховка на случай, если Netflix позже задним числом "
        "изменит цифру, а у тебя уже зафиксированы реальные данные "
        "после озвучки."
    )

    _h3(doc, "Sticky-шапка")
    _p(
        doc,
        "При прокрутке длинной таблицы вверху остаются прилеплены: "
        "топбар DubStudio, заголовок проекта с кнопками Report / "
        "Export / Import EPs, табы Transcription/Dialog/Report и шапка "
        "самой таблицы."
    )

    _h3(doc, "Тёмная тема")
    _p(
        doc,
        "Сохраняется в браузере (localStorage), не на сервере. На "
        "другом устройстве или в режиме инкогнито нужно включить "
        "заново."
    )

    _h3(doc, "Случайно удалил/неправильно слил персонажа")
    _p(
        doc,
        "Откатить нельзя — только повторно импортировать xlsx-файл с "
        "тем эпизодом. При импорте удалённый персонаж появится снова "
        "(как новый, с красным значком). Word count'ы вернутся; "
        "actor-assignment придётся проставить заново."
    )

    # ---- save ---------------------------------------------------------
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    doc.save(OUTPUT)
    return OUTPUT


if __name__ == "__main__":
    path = build()
    print(f"Wrote {path}")
